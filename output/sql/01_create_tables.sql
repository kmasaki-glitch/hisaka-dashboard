-- Hisaka Demo: Table definitions
-- Dimension tables

CREATE OR REPLACE TABLE DIM_SEGMENT (
    ID VARCHAR,
    NAME VARCHAR,
    CODE VARCHAR,
    REVENUE_RATIO FLOAT
);

CREATE OR REPLACE TABLE DIM_PRODUCT (
    ID VARCHAR,
    NAME VARCHAR,
    SEGMENT_ID VARCHAR,
    CATEGORY VARCHAR,
    AVG_PRICE INTEGER,
    MAINT_INTERVAL_MONTHS INTEGER
);

CREATE OR REPLACE TABLE DIM_ACCOUNT (
    ID VARCHAR,
    NAME VARCHAR,
    INDUSTRY VARCHAR,
    REGION VARCHAR,
    SIZE VARCHAR,
    SEGMENT_IDS VARCHAR
);

CREATE OR REPLACE TABLE DIM_SALES_REP (
    ID VARCHAR,
    NAME VARCHAR,
    SEGMENT_ID VARCHAR,
    YEARS INTEGER,
    ROLE VARCHAR
);

CREATE OR REPLACE TABLE DIM_STAGE (
    ID INTEGER,
    NAME VARCHAR,
    PROBABILITY INTEGER,
    AVG_DAYS INTEGER
);

CREATE OR REPLACE TABLE DIM_DATE (
    DATE_KEY DATE,
    YEAR INTEGER,
    MONTH INTEGER,
    DAY INTEGER,
    WEEKDAY VARCHAR,
    IS_WEEKDAY INTEGER,
    FISCAL_YEAR INTEGER,
    FISCAL_QUARTER VARCHAR,
    FISCAL_YEAR_LABEL VARCHAR
);

-- Fact tables

CREATE OR REPLACE TABLE FACT_OPPORTUNITY (
    OPP_ID VARCHAR,
    DATE_KEY DATE,
    ACCOUNT_ID VARCHAR,
    SEGMENT_ID VARCHAR,
    PRODUCT_ID VARCHAR,
    SALES_REP_ID VARCHAR,
    OPP_NAME VARCHAR,
    STAGE VARCHAR,
    STAGE_ID INTEGER,
    AMOUNT INTEGER,
    PROBABILITY INTEGER,
    EXPECTED_CLOSE_DATE DATE,
    CLOSE_DATE DATE,
    DAYS_IN_STAGE INTEGER,
    IS_WON INTEGER,
    IS_LOST INTEGER,
    IS_OPEN INTEGER,
    COMPETITOR VARCHAR,
    CROSS_SELL_POTENTIAL INTEGER,
    ACTIVITY_COUNT INTEGER,
    CREATED_DATE DATE
);

CREATE OR REPLACE TABLE FACT_SERVICE_CASE (
    CASE_ID VARCHAR,
    DATE_KEY DATE,
    ACCOUNT_ID VARCHAR,
    SEGMENT_ID VARCHAR,
    PRODUCT_ID VARCHAR,
    RELATED_OPP_ID VARCHAR,
    CASE_TYPE VARCHAR,
    PRIORITY VARCHAR,
    STATUS VARCHAR,
    RESOLUTION_HOURS INTEGER,
    SLA_TARGET_HOURS INTEGER,
    SLA_MET INTEGER,
    REVENUE INTEGER,
    COST INTEGER,
    GROSS_PROFIT INTEGER,
    SATISFACTION_SCORE INTEGER
);
