-- Hisaka Demo: Database, Schema, Warehouse, Stage setup
CREATE DATABASE IF NOT EXISTS HISAKA_DEMO_DB;
USE DATABASE HISAKA_DEMO_DB;
CREATE SCHEMA IF NOT EXISTS SALES;
USE SCHEMA SALES;
CREATE WAREHOUSE IF NOT EXISTS HISAKA_DEMO_WH WITH WAREHOUSE_SIZE='XSMALL' AUTO_SUSPEND=60 AUTO_RESUME=TRUE;
CREATE OR REPLACE STAGE HISAKA_CSV_STAGE FILE_FORMAT=(TYPE='CSV' FIELD_OPTIONALLY_ENCLOSED_BY='"' SKIP_HEADER=1 FIELD_DELIMITER=',' NULL_IF=(''));
